/**
 * 验证密码 -> 密码包含大写字母、小写字母、数字、特殊字符中的三种，长度8-16位
 * @param {*} val
 */
export function validatePwd(val) {
  return /^(?![a-zA-Z]+$)(?![A-Z0-9]+$)(?![A-Z\W_!@#$%^&*`~()-+=]+$)(?![a-z0-9]+$)(?![a-z\W_!@#$%^&*`~()-+=]+$)(?![0-9\W_!@#$%^&*`~()-+=]+$)[a-zA-Z0-9\W_!@#$%^&*`~()-+=]{8,16}$/.test(
    val,
  )
}
